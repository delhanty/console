export function formatWebLink(value) {
  // little sanity check here...
  if (!value.startsWith('http')) {
    return 'about:blank';
  }
  return value;
}

export function formatTCPPort(port) {
  let found = TCPPortMap.find(e => e.port === port);
  if (found !== null) {
    return found.service;
  }
  return 'Unknown';
}
export function formatNSRecord(record) {
  let found = NSRecords.find(e => e.value === record);
  if (found !== null) {
    return found.record;
  }
  return 'Unknown';
}
